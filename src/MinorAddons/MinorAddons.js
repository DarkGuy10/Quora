// Go back, forward, and reload btns

var addedNavBar = document.createElement('div');
addedNavBar.classList.add('addedNavBar');

var backBtn = document.createElement('button');
backBtn.classList.add('addedNavBtn');
backBtn.innerHTML = "<i class='fas fa-arrow-left fa-fw'></i>";

var forwardBtn = document.createElement('button');
forwardBtn.classList.add('addedNavBtn');
forwardBtn.innerHTML = "<i class='fas fa-arrow-right fa-fw'></i>";

var reloadBtn = document.createElement('button');
reloadBtn.classList.add('addedNavBtn');
reloadBtn.innerHTML = "<i class='fas fa-redo-alt fa-fw'></i>";

addedNavBar.appendChild(backBtn);
addedNavBar.appendChild(forwardBtn);
addedNavBar.appendChild(reloadBtn);
document.body.appendChild(addedNavBar);

backBtn.onclick = () => {
	window.history.back();
};

forwardBtn.onclick = () => {
	window.history.back();
};

reloadBtn.onclick = () => {
	window.reload();
};